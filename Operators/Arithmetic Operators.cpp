//Arithmetic Operator

#include<iostream>
using namespace std;

int main(){

   int num1 = 4;
   int num2 = 8;

   cout<<num1+num2<<endl;    //12
   cout<<num1-num2<<endl;    //-4
   cout<<num1*num2<<endl;    //32
   cout<<num1/num2<<endl;    //0
   cout<<num1%num2<<endl;    //4
   cout<<num1++;             //5
   cout<<num1--;             //4


   return 0;


}


